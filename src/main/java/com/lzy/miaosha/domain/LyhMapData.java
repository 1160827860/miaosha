package com.lzy.miaosha.domain;

public class LyhMapData {
    private String value;
    private int indexs;
    private int stateInitColor;
    private String city;

    public int getIndexs() {
        return indexs;
    }

    public void setIndexs(int indexs) {
        this.indexs = indexs;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }



    public int getStateInitColor() {
        return stateInitColor;
    }

    public void setStateInitColor(int stateInitColor) {
        this.stateInitColor = stateInitColor;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
